// Arithmetic Operators in JS
let a = 10;
let b = 5;

// Addition
let sum = a + b;
console.log("Sum:", sum);

// Subtraction
let substract = a - b;
console.log("Subtraction:", substract);

// Multiplication
let product = a * b;
console.log("Product:", product);

// Division
let Division = a / b;
console.log("Quotient:", Division);

// Modulo (Remainder)
let remainder = a % b;
console.log("Remainder:", remainder);

// Exponentiation
let exponent = a ** b;
console.log("Exponent:", exponent);



// Logical Operators in JS
let x = true;
let y = false;

// AND
let andResult = x && y;
console.log("AND Result:", andResult);

// OR
let orResult = x || y;
console.log("OR Result:", orResult);

// NOT
let notResult = !x;
console.log("NOT Result:", notResult);


